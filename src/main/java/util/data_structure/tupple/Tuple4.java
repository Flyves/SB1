package util.data_structure.tupple;

public class Tuple4<T, U, V, W> {
    public T value1;
    public U value2;
    public V value3;
    public W value4;

    public Tuple4(final T value1, final U value2, final V value3, final W value4) {
        this.value1 = value1;
        this.value2 = value2;
        this.value3 = value3;
        this.value4 = value4;
    }
}
